//! Stage 1 of docs/design/contract-driven-implementation-experiment.md: Single-Contract Parallel
//! Implementation Trial.
//!
//! **This is an experimental probe, not production wiring.** It does not call, modify, or import
//! anything from `canopy-cli`'s `implement` command, `plan.rs`, `execute.rs`, or `step.rs`. It
//! runs entirely as a standalone binary (a cargo example — not part of the `canopy` CLI or the
//! `canopy-llm` library's public surface) and changes no existing behavior.
//!
//! ## What's reused vs. what's experimental
//!
//! Reused, unmodified, from production code:
//! - `canopy_storage::load_contracts` — the same function `canopy behaviors` itself uses to save
//!   what this loads back.
//! - `canopy_llm::resolve_implementation_target` / `abstract_layer_for_kind` — the mechanical
//!   file-target resolver from this investigation's second grounding slice.
//! - `canopy_llm::skill_for_technology` — the real, unmodified tech-stack skill renderer.
//!
//! Experimental, novel to this file:
//! - The prompts themselves (`test_prompt`/`impl_prompt` below) — deliberately narrower than
//!   `canopy-llm/src/prompts/step.rs`'s real prompts, restricted to exactly the inputs
//!   `docs/design/contract-driven-implementation-experiment.md` §4 allows.
//! - Contract selection (by `entity`/`member`/`kind`, not by hardcoded id, so this keeps working
//!   if `manufacturer-001-contract-001`'s numeric id ever shifts on a re-run).
//!
//! ## Inputs allowed (design doc §4) vs. disallowed (§5)
//!
//! Allowed and used: the selected contract (`kind`/`entity`/`member`/`mandatory`/
//! `required_tests`/`dependencies`), any dependency contracts (none exist for the contract
//! selected below — this contract's `dependencies` is empty), the resolved implementation
//! target, the tech-stack skill for the resolved layer.
//!
//! Explicitly NOT read anywhere in this file: the user story, `spec.yaml`'s `scenarios` or
//! `entity_schema`, any ADR, the OpenAPI spec, or any tool (`find_symbol`/`read_file`). If any of
//! those were accidentally pulled in, grep this file for `load_story_spec`, `load_all_adrs`,
//! `load_story_openapi`, `load_user_stories`, or `ToolSpec` — none appear.
//!
//! ## Run
//!
//! Requires a real `contracts.yaml` already generated by `canopy behaviors <story-id>` (this
//! experiment does not generate one — see docs/contract-readiness-assessment.md for how
//! `manufacturer-001`'s was produced) and a local llama-server on :8080 (this project's
//! dogfooding model).
//!
//! ```sh
//! cargo run -p canopy-llm --example contract_driven_stage1_experiment -- <project-root> <story-id>
//! ```
//! `<project-root>` defaults to `.` (the current directory) if omitted; `<story-id>` defaults to
//! `manufacturer-001`.

use canopy_core::{AgentLlmConfig, BehaviorKind, Contract, LlmProvider};
use canopy_llm::{abstract_layer_for_kind, resolve_implementation_target, skill_for_technology, LlmClient};

/// Picks the contract this trial targets: a single-field, mandatory, dependency-free unit
/// validation contract — the smallest, least ambiguous case, matching the mandatory/optional
/// probe's own chosen complexity level (docs/contract-readiness-assessment.md Addendum 3).
/// Selected by (`kind`, `member`) rather than a hardcoded contract id, so this doesn't break if
/// contract numbering shifts on a re-run.
fn select_contract<'a>(contracts: &'a [Contract], member: &str) -> Option<&'a Contract> {
    contracts.iter().find(|c| {
        c.kind.as_ref() == Some(&BehaviorKind::Validation) && c.member.as_deref() == Some(member)
    })
}

fn render_contract_facts(contract: &Contract) -> String {
    format!(
        "Entity: {entity}\n\
         Field: {member}\n\
         Kind: Validation\n\
         Mandatory: {mandatory}\n\
         \n\
         Required behavior (verbatim — the only source of truth for what this field must do):\n\
         {tests}",
        entity = contract.entity.as_deref().unwrap_or("<unknown>"),
        member = contract.member.as_deref().unwrap_or("<unknown>"),
        mandatory = contract.mandatory.map(|m| m.to_string()).unwrap_or_else(|| "<unknown>".to_string()),
        tests = contract.required_tests.iter().map(|t| format!("- \"{t}\"")).collect::<Vec<_>>().join("\n"),
    )
}

fn test_prompt(contract: &Contract, target_path: &str, tech_rules: &str) -> String {
    format!(
        "You are generating a JUnit 5 test class, using ONLY the information below — do not \
         assume any requirement not explicitly stated here, and do not test any field other than \
         the one named below.\n\
         \n\
         {facts}\n\
         \n\
         Implementation file (not yet written): {target_path}\n\
         \n\
         {tech_rules}\n\
         \n\
         Write ONLY a test class covering the required behavior above for this ONE field — one \
         @Test method per required-behavior line, nothing else.\n",
        facts = render_contract_facts(contract),
        target_path = target_path,
        tech_rules = tech_rules,
    )
}

fn impl_prompt(contract: &Contract, target_path: &str, tech_rules: &str, test_code: &str) -> String {
    format!(
        "You are generating exactly one file, using ONLY the information below — do not assume \
         any requirement not explicitly stated here.\n\
         \n\
         {facts}\n\
         \n\
         Target file: {target_path}\n\
         \n\
         This file may eventually need to satisfy OTHER fields/contracts not shown to you here — \
         implement ONLY what THIS contract requires. Do NOT invent unrelated fields, methods, or \
         class structure beyond the minimum needed to declare this one field and make the test \
         below compile and pass.\n\
         \n\
         {tech_rules}\n\
         \n\
         Test this file must satisfy:\n\
         ```java\n{test_code}\n```\n\
         \n\
         Write the complete file content.\n",
        facts = render_contract_facts(contract),
        target_path = target_path,
        tech_rules = tech_rules,
        test_code = test_code,
    )
}

fn main() {
    let mut args = std::env::args().skip(1);
    let project_root = args.next().unwrap_or_else(|| ".".to_string());
    let story_id = args.next().unwrap_or_else(|| "manufacturer-001".to_string());

    std::env::set_current_dir(&project_root)
        .unwrap_or_else(|e| panic!("cannot cd into '{project_root}': {e}"));

    let contracts = canopy_storage::load_contracts(&story_id)
        .unwrap_or_else(|e| panic!("failed to load contracts.yaml for '{story_id}': {e}"));

    let contract = select_contract(&contracts.contracts, "name")
        .unwrap_or_else(|| panic!("no single-field mandatory validation contract for member 'name' found in {story_id}'s contracts.yaml"));

    println!("=== Selected contract ===\n");
    println!("id:           {}", contract.id);
    println!("name:         {}", contract.name);
    println!("kind:         {:?}", contract.kind);
    println!("entity:       {:?}", contract.entity);
    println!("member:       {:?}", contract.member);
    println!("mandatory:    {:?}", contract.mandatory);
    println!("dependencies: {:?}", contract.dependencies);
    println!("required_tests:");
    for t in &contract.required_tests {
        println!("  - {t}");
    }
    println!();

    if !contract.dependencies.is_empty() {
        println!("=== Dependency contracts ===\n");
        for dep_id in &contract.dependencies {
            if let Some(dep) = contracts.contracts.iter().find(|c| &c.id == dep_id) {
                println!("- {} (entity={:?}, member={:?})", dep.id, dep.entity, dep.member);
            }
        }
        println!();
    } else {
        println!("=== Dependency contracts: none ===\n");
    }

    let entity = contract.entity.as_deref().unwrap_or("Manufacturer");
    let kind = contract.kind.clone().unwrap_or(BehaviorKind::Validation);
    let layer = abstract_layer_for_kind(&kind);
    // Mirrors plan.rs's own fallback for an un-scaffolded JVM service (service name with
    // hyphens replaced by underscores) — no scaffold has been run for this dogfooding project
    // yet, so there is no detected package to read.
    let pkg = "manufacturer_service";
    let target_path = resolve_implementation_target("Spring Boot", pkg, "manufacturer-service", layer, entity, None)
        .unwrap_or_else(|| panic!("no mechanical target for layer '{layer}'"));

    println!("=== Resolved implementation target ===\n");
    println!("{target_path}\n");

    let tech_rules = skill_for_technology("Spring Boot", pkg, pkg, "manufacturer-service", layer);

    let client = LlmClient::from_agent_config(
        &AgentLlmConfig {
            provider: LlmProvider::Ollama,
            model: "qwen2.5-coder:14b".to_string(),
            base_url: Some("http://localhost:8080".to_string()),
        },
        false,
    );

    println!("=== Generated test ===\n");
    let t_prompt = test_prompt(contract, &target_path, &tech_rules);
    let test_code = client.complete_large(&t_prompt).unwrap_or_else(|e| panic!("test generation failed: {e}"));
    println!("{test_code}\n");

    println!("=== Generated implementation ===\n");
    let i_prompt = impl_prompt(contract, &target_path, &tech_rules, &test_code);
    let impl_code = client.complete_large(&i_prompt).unwrap_or_else(|e| panic!("implementation generation failed: {e}"));
    println!("{impl_code}\n");
}
